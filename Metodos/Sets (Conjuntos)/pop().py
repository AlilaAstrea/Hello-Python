'''The pop() method removes and returns the arbitrary element elements from the set. Raises KeyError if the set is empty'''
'''Elimina y retorna un elemento aleatorio del set.'''

a = {1,2,3}
x = a.pop()
print(x)
print(a) #output = 1 and {2,3}


s = {1, 2, 3}
valor = s.pop()
print(s)  # {2, 3} o similar
print(valor)  # 1 o similar
