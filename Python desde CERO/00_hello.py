# Consultar el tipo de dato
print(type("Soy un dato str"))    # Tipo 'str'     <class 'str'>
print(type(23))                   # Tipo 'int'     <class 'int'>
print(type(2.3))                  # Tipo 'float'   <class 'float'>
print(type(True))                 # Tipo 'bool'    <class 'bool'>
print(type(1 + 3j))               # Tipo 'complex' <class 'complex'>
print(type([1,2,3]))              # Tipo 'list'    <class 'list'>
print(type({'nombre': 'Matiti'})) # Tipo 'dict'    <class 'dict'>
print(type({9.8, 3.14, 2.4}))     # Tipo 'set'     <class 'set'>
print(type((9.8, 3.14, 2.4)))     # Tipo 'tuple'   <class 'tuple'>

