

'''
algoritmo es un método para resolver problemas compuesto
de instrucciones que s erealizan paso a paso para conseguir una solución

- "Diseño de algoritmo" describe la secuencia ordenada de pasos, sin 
ambiguedades, que conducen a la solucion de un problema

- Luego se expresa el algoritmo como un programa en un lenguaje de progr.

- Ejecución y validación del programa de computadora

'''

'''
Los algoritmos son independietes tanto del lenguaje de programación en que
se expresan como de la computadora que los ejecuta.
Es decir, que, sin importar el lenguaje y computadora, el algoritmo
siempre será el mismo.

Un Algoritmo debe ser preciso e indicar el orden de realización de cada paso

Debe ser definido. Todas las veces que se ejecuta se debe lograr el mismo resultado

Debe ser finito. Se debe terminar en algún momento.

El Algoritmo tiene tres partes. Entrada - Proceso - Salida.

Se debe tener en mente a la hora de crear un algoritmo la frase
"Divide y vencerás" ya que consiste en dividir un problema complejo
en otros más simples

La descomposición de un problema original en subproblemas más simples
( se Conoce como diseño descendiente o [top-down desing])

'''

